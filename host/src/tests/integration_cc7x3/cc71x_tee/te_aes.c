static TE_rc_t AES_exec(TE_TestVec_t *pTestVec, TE_TestRc_t *pVecResult)
{
    TE_perfIndex_t cookie = 0;
    uint32_t numOfBlocks = 0;
    uint32_t remainDataSize = 0;
    uint32_t i = 0;
    size_t dataOutSize = 0;
    CCAesUserContext_t iContext;
    uint8_t *blockOutput = NULL;
    uint8_t* text = NULL;
    TE_rc_t res = TE_RC_SUCCESS;
    aesVector_t* testVec = NULL;

    if (pVecResult == NULL) {
        res = TE_RC_FAIL;
        TE_LOG_ERROR("Invalid params! (pVecResult == NULL)\n");
        return res;
    }
    if (pTestVec == NULL) {
        res = TE_RC_FAIL;
        *pVecResult = TE_RC_FAIL;
        TE_LOG_ERROR("Invalid params! (pTestVec == NULL)\n");
        goto bail;
    }

    TE_ALLOC(blockOutput, sizeof(testVec->cipherText));
    testVec = (aesVector_t *)pTestVec->pData;
    text = testVec->encryptDecryptFlag == CC_AES_ENCRYPT ? testVec->plaintText : testVec->cipherText;


    cookie = TE_perfOpenNewEntry("aes", "init");
    TE_ASSERT(CC_AesInit(&iContext,
                         testVec->encryptDecryptFlag,
                         testVec->operationMode,
                         testVec->paddingType) == CC_OK);
    TE_perfCloseEntry(cookie);

    cookie = TE_perfOpenNewEntry("aes", "setkey");
    TE_ASSERT(CC_AesSetKey(&iContext,
                           CC_AES_USER_KEY,
                           (&testVec->pKeyData),
                           sizeof(testVec->pKeyData)) == CC_OK);
    TE_perfCloseEntry(cookie);

    if ((testVec->operationMode == CC_AES_MODE_CBC)
                    || (testVec->operationMode == CC_AES_MODE_CTR)
                    || (testVec->operationMode == CC_AES_MODE_CBC_CTS)
                    || (testVec->operationMode == CC_AES_MODE_XTS)
                    || (testVec->operationMode == CC_AES_MODE_OFB)) {
        cookie = TE_perfOpenNewEntry("aes", "setiv");
        TE_ASSERT(CC_AesSetIv(&iContext, testVec->ivBuffer) == CC_OK);
        TE_perfCloseEntry(cookie);
    }

    numOfBlocks = sizeof(testVec->plaintText) / CC_AES_BLOCK_SIZE_IN_BYTES;
    remainDataSize = (sizeof(testVec->plaintText) % CC_AES_BLOCK_SIZE_IN_BYTES);

    cookie = TE_perfOpenNewEntry("aes", "block");
    for (i = 0; i < numOfBlocks; ++i) {
        TE_ASSERT(CC_AesBlock(&iContext,
                              SET_IN_BUFF_PTR(text, i, CC_AES_BLOCK_SIZE_IN_BYTES),
                              CC_AES_BLOCK_SIZE_IN_BYTES,
                              SET_OUT_BUFF_PTR(blockOutput, i, CC_AES_BLOCK_SIZE_IN_BYTES)) == CC_OK);
    }
    TE_perfCloseEntry(cookie);

    cookie = TE_perfOpenNewEntry("aes", "finish");
    TE_ASSERT(CC_AesFinish(&iContext,
                           remainDataSize,
                           ((remainDataSize > 0) ? (SET_IN_BUFF_PTR(text, i, CC_AES_BLOCK_SIZE_IN_BYTES)) : NULL),
                           remainDataSize,
                           SET_OUT_BUFF_PTR(blockOutput, i, CC_AES_BLOCK_SIZE_IN_BYTES),
                           &dataOutSize) == CC_OK);
    TE_perfCloseEntry(cookie);

    cookie = TE_perfOpenNewEntry("aes", "free");
    TE_ASSERT(CC_AesFree(&iContext) == CC_OK);
    TE_perfCloseEntry(cookie);

    TE_LOG_BUFF(debug, blockOutput, sizeof(testVec->cipherText));
    TE_LOG_BUFF(debug,
                (testVec->encryptDecryptFlag == CC_AES_ENCRYPT ? testVec->cipherText : testVec->plaintText),
                sizeof(testVec->cipherText));

    TE_ASSERT(memcmp(blockOutput,
                     (testVec->encryptDecryptFlag == CC_AES_ENCRYPT ? testVec->cipherText : testVec->plaintText),
                     sizeof(testVec->cipherText)) == 0);

bail:
    *pVecResult = res;
    TE_FREE(blockOutput);
    return res;
}


/******************************************************************
 * Public
 ******************************************************************/
int TE_init_aes_test(void)
{
    TE_rc_t res = TE_RC_SUCCESS;

    TE_perfEntryInit("aes", "init");
    TE_perfEntryInit("aes", "setkey");
    TE_perfEntryInit("aes", "setiv");
    TE_perfEntryInit("aes", "block");
    TE_perfEntryInit("aes", "finish");
    TE_perfEntryInit("aes", "free");

    TE_ASSERT(TE_registerSuite("aes-test-1",
                               "ecb mode",
                               "en/decrypt",
                               NULL,
                               AES_exec,
                               NULL,
                               &ecb_vec_list) == TE_RC_SUCCESS);

    TE_ASSERT(TE_registerSuite("aes-test-1",
                               "cbc mode",
                               "en/decrypt",
                               NULL,
                               AES_exec,
                               NULL,
                               &cbc_vec_list) == TE_RC_SUCCESS);

    TE_ASSERT(TE_registerSuite("aes-test-1",
                               "ctr mode",
                               "en/decrypt",
                               NULL,
                               AES_exec,
                               NULL,
                               &ctr_vec_list) == TE_RC_SUCCESS);

    TE_ASSERT(TE_registerSuite("aes-test-1",
                               "ofb mode",
                               "en/decrypt",
                               NULL,
                               AES_exec,
                               NULL,
                               &ofb_vec_list) == TE_RC_SUCCESS);

bail:
    return res;
}
