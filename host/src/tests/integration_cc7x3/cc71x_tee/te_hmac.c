static TE_rc_t hmac_integral_exec(TE_TestVec_t *pTestVec, TE_rc_t *pTestResult);

static TE_rc_t hmac_non_integral_exec(TE_TestVec_t *pTestVec, TE_rc_t *pTestResult);

/******************************************************************
 * Public
 ******************************************************************/
static TE_rc_t hmac_integral_exec(TE_TestVec_t *pTestVec, TE_rc_t *pTestResult)
{
    TE_rc_t res = TE_RC_SUCCESS;
    TE_perfIndex_t cookie = 0;
    hmacVector_t* pHmacVec = NULL;
    CCHashOperationMode_t hashMode = 0;
    CCHashResultBuf_t resBuff = { 0 };

    if (pTestResult == NULL) {
        res = TE_RC_FAIL;
        TE_LOG_ERROR("Invalid params! (pTestResult == NULL)\n");
        return res;
    }
    TE_ASSERT(pTestVec != NULL);

    pHmacVec = pTestVec->pData;
    hashMode = pHmacVec->hashMode;

    TE_ASSERT(hashMode < CC_HASH_NumOfModes);

    /* Hashing */
    /*---------*/
    cookie = TE_perfOpenNewEntry("hmac", "integral");
    TE_ASSERT(CC_Hmac(hashMode, pHmacVec->key, pHmacVec->keySize, pHmacVec->plaintext, pHmacVec->plaintextSize, resBuff) == CC_OK);
    TE_perfCloseEntry(cookie);

    /* Verifying hashed data is as expected */
    /*--------------------------------------*/
    TE_ASSERT(memcmp(resBuff, pHmacVec->expectedOutput, pHmacVec->outputSize) == 0);

bail:
    *pTestResult = res;
    return res;
}

static TE_rc_t hmac_non_integral_exec(TE_TestVec_t *pTestVec, TE_rc_t *pTestResult)
{
    TE_rc_t res = TE_RC_SUCCESS;
    TE_perfIndex_t cookie = 0;
    hmacVector_t* pHmacVec = NULL;
    CCHashOperationMode_t hashMode = 0;
    CCHashResultBuf_t resBuff = { 0 };
    CCHmacUserContext_t userContext;

    if (pTestResult == NULL) {
        res = TE_RC_FAIL;
        TE_LOG_ERROR("Invalid params! (pTestResult == NULL)\n");
        return res;
    }
    TE_ASSERT(pTestVec != NULL);

    pHmacVec = pTestVec->pData;
    hashMode = pHmacVec->hashMode;

    TE_ASSERT(hashMode < CC_HASH_NumOfModes);

    /* Init */
    /*------*/
    cookie = TE_perfOpenNewEntry("hmac", "init");
    TE_ASSERT(CC_HmacInit(&userContext, hashMode, pHmacVec->key, pHmacVec->keySize) == CC_OK);
    TE_perfCloseEntry(cookie);

    /* Update */
    /*--------*/
    cookie = TE_perfOpenNewEntry("hmac", "update");
    TE_ASSERT(CC_HmacUpdate(&userContext, pHmacVec->plaintext, pHmacVec->plaintextSize) == CC_OK);
    TE_perfCloseEntry(cookie);

    /* Finish */
    /*--------*/
    cookie = TE_perfOpenNewEntry("hmac", "finish");
    TE_ASSERT(CC_HmacFinish(&userContext, resBuff) == CC_OK);
    TE_perfCloseEntry(cookie);

    /* Free */
    /*------*/
    cookie = TE_perfOpenNewEntry("hmac", "free");
    TE_ASSERT(CC_HmacFree(&userContext) == CC_OK);
    TE_perfCloseEntry(cookie);

    /* Verifying hashed data is as expected */
    /*--------------------------------------*/
    TE_ASSERT(memcmp(resBuff, pHmacVec->expectedOutput, pHmacVec->outputSize) == 0);

bail:
    *pTestResult = res;
    return res;
}

int TE_init_hmac_test(void)
{
    TE_rc_t res = TE_RC_SUCCESS;

    TE_perfEntryInit("hmac", "integral");
    TE_perfEntryInit("hmac", "init");
    TE_perfEntryInit("hmac", "update");
    TE_perfEntryInit("hmac", "finish");
    TE_perfEntryInit("hmac", "free");

    /* Hmac integral test */
    /*--------------------*/
    TE_ASSERT(TE_registerSuite("hmac",
                              "hmac&verify",
                              "integral",
                              NULL,
                              hmac_integral_exec,
                              NULL,
                              &testVecListHMAC) == TE_RC_SUCCESS);

    /* Hmac non-integral test */
    /*------------------------*/
    TE_ASSERT(TE_registerSuite("hmac",
                               "hmac&verify",
                               "non-integral",
                               NULL,
                               hmac_non_integral_exec,
                               NULL,
                               &testVecListHMAC) == TE_RC_SUCCESS);

bail:
    return res;
}
